package com.rav.sbactivemq.sbactivemqconsumer;

public class Movie {
	public String title;
	public int year;
	
	public Movie() {
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
}
